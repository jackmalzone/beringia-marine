/**
 * Get all published articles, sorted by publish date (newest first)
 * Filters out drafts and scheduled articles that haven't reached their publish date
 */
export function getAllArticles(): ArticleData[] {
  const now = new Date();
  return mockArticles
    .filter(article => {
      // Only show published articles or scheduled articles past their publishAt date
      if (article.status === 'draft') return false;
      if (article.status === 'scheduled' && article.publishAt) {
        return new Date(article.publishAt) <= now;
      }
      return article.status === 'published';
    })
    .sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime());
}

/**
 * Get a single article by its slug
 * Returns undefined if article doesn't exist, is a draft, or is scheduled for future
 */
export function getArticleBySlug(slug: string): ArticleData | undefined {
  const article = mockArticles.find(article => article.slug === slug);
  if (!article || article.status === 'draft') return undefined;

  // Check if scheduled article is ready to publish
  if (article.status === 'scheduled' && article.publishAt) {
    if (new Date(article.publishAt) > new Date()) return undefined;
  }

  return article;
}

/**
 * Get articles filtered by category
 * Returns all published articles if category is "All"
 */
export function getArticlesByCategory(category: string): ArticleData[] {
  if (category === 'All') return getAllArticles();
  return getAllArticles().filter(article => article.category === category);
}

/**
 * Get only categories that have published articles
 * Useful for dynamically showing only relevant category filters
 */
export function getActiveCategories(): ArticleCategory[] {
  const articles = getAllArticles();
  const categoriesWithContent = new Set(articles.map(a => a.category));
  return ARTICLE_CATEGORIES.filter(cat => categoriesWithContent.has(cat));
}

/**
 * Search articles by title, abstract, or tags (case-insensitive fuzzy search)
 * Only searches through published articles
 */
export function searchArticles(query: string): ArticleData[] {
  if (!query.trim()) return getAllArticles();

  const lowerQuery = query.toLowerCase().trim();
  return getAllArticles().filter(
    article =>
      article.title.toLowerCase().includes(lowerQuery) ||
      article.abstract.toLowerCase().includes(lowerQuery) ||
      article.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}

/**
 * Calculate estimated reading time based on content word count
 * Assumes average reading speed of 200 words per minute
 */
export function calculateReadingTime(content: string): number {
  const wordsPerMinute = 200;
  // Strip HTML tags to get plain text
  const textContent = content.replace(/<[^>]*>/g, '');
  // Count words (split by whitespace)
  const wordCount = textContent.trim().split(/\s+/).length;
  // Calculate minutes, minimum 1 minute
  return Math.max(1, Math.ceil(wordCount / wordsPerMinute));
}
